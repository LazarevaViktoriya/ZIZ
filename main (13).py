import random

# Создаем список поздравлений
greetings = [
    "С днем рождения! Желаем тебе всего самого наилучшего!",
    "Поздравляем с днем рождения! Желаем здоровья, счастья и любви!",
    "С днем рождения! Пусть в жизни все будет прекрасно!",
    "С днем рождения! Желаем успехов в работе и личной жизни!",
    "Поздравляем с днем рождения! Пусть все мечты сбываются!",
]

# Получаем имя пользователя
name = input("Как тебя зовут? ")

# Генерируем случайное поздравление
greeting = random.choice(greetings)

# Выводим поздравление на экран
print(f"{greeting}, {name}!")

